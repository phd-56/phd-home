<script setup lang="ts">
import { ref, reactive } from 'vue'
import { useRouter } from 'vue-router'
import { ElMessage } from 'element-plus'
import { useAuthStore } from '@/stores/auth'

const router = useRouter()
const authStore = useAuthStore()

const loading = ref(false)

interface LoginForm {
  role: 'patient' | 'doctor' | 'admin'
  username: string
  password: string
  remember: boolean
}

const form = reactive<LoginForm>({
  role: 'doctor',
  username: '',
  password: '',
  remember: false
})

const handleLogin = async () => {
  try {
    if (!form.username || !form.password) {
      ElMessage.warning('è¯·å¡«å†™å®Œæ•´ä¿¡æ¯')
      return
    }

    loading.value = true

    console.log('å¼€å§‹ç™»å½•...', form)

    await authStore.login(form.username, form.password, form.role)
    
    ElMessage.success('ç™»å½•æˆåŠŸï¼')

    if (authStore.user && authStore.isAuthenticated) {
      const targetRoute = getTargetRoute(authStore.user.role)
      
      setTimeout(() => {
        router.push(targetRoute)
      }, 100)
    } else {
      ElMessage.error('ç™»å½•çŠ¶æ€å¼‚å¸¸ï¼Œè¯·é‡è¯•')
    }
    
  } catch (error: any) {
    console.error('ç™»å½•é”™è¯¯:', error)
    ElMessage.error(error.message || 'ç™»å½•å¤±è´¥ï¼Œè¯·æ£€æŸ¥ç”¨æˆ·åå’Œå¯†ç ')
  } finally {
    loading.value = false
  }
}

const getTargetRoute = (role: string): string => {
  switch (role) {
    case 'patient': return '/dashboard/patient'
    case 'doctor': return '/dashboard/doctor'
    case 'admin': return '/dashboard/admin/user-management'
    default: return '/dashboard/patient'
  }
}
