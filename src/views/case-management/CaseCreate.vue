const handleDrop = (e: DragEvent) => {
  const uploadArea = e.currentTarget as HTMLElement
  uploadArea.style.borderColor = '#d9d9d9'
  uploadArea.style.background = '#fafafa'
  
  const files = Array.from(e.dataTransfer?.files || [])
